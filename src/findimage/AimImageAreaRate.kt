package findimage

class AimImageAreaRate(startPointXRate: Float, startPointYRate: Float, endPointXRate: Float, endPointYRate: Float) {
    val startPointXRate: Float
    val startPointYRate: Float
    val endPointXRate: Float
    val endPointYRate: Float

    init {
        this.startPointXRate = startPointXRate
        this.startPointYRate = startPointYRate
        this.endPointXRate = endPointXRate
        this.endPointYRate = endPointYRate
    }
}